package com.genius.search.search.actionbean;

public class Builder2ActionBean {

	private String fieldShow;
	private String fieldValue;
	private String fieldOfTable;
	private String fieldType;
	private String fieldOptionValue;
	private String fieldId;
	
	/**
	 * @return 返回 fieldId。
	 */
	public String getFieldId() {
		return fieldId;
	}
	/**
	 * @param fieldId 要设置的 fieldId。
	 */
	public void setFieldId(String fieldId) {
		this.fieldId = fieldId;
	}
	/**
	 * @return 返回 fieldOptionValue。
	 */
	public String getFieldOptionValue() {
		return fieldOptionValue;
	}
	/**
	 * @param fieldOptionValue 要设置的 fieldOptionValue。
	 */
	public void setFieldOptionValue(String fieldOptionValue) {
		this.fieldOptionValue = fieldOptionValue;
	}
	/**
	 * @return 返回 fieldType。
	 */
	public String getFieldType() {
		return fieldType;
	}
	/**
	 * @param fieldType 要设置的 fieldType。
	 */
	public void setFieldType(String fieldType) {
		this.fieldType = fieldType;
	}
	/**
	 * @return 返回 fieldOfTable。
	 */
	public String getFieldOfTable() {
		return fieldOfTable;
	}
	/**
	 * @param fieldOfTable 要设置的 fieldOfTable。
	 */
	public void setFieldOfTable(String fieldOfTable) {
		this.fieldOfTable = fieldOfTable;
	}
	/**
	 * @return 返回 fieldShow。
	 */
	public String getFieldShow() {
		return fieldShow;
	}
	/**
	 * @param fieldShow 要设置的 fieldShow。
	 */
	public void setFieldShow(String fieldShow) {
		this.fieldShow = fieldShow;
	}
	/**
	 * @return 返回 fieldValue。
	 */
	public String getFieldValue() {
		return fieldValue;
	}
	/**
	 * @param fieldValue 要设置的 fieldValue。
	 */
	public void setFieldValue(String fieldValue) {
		this.fieldValue = fieldValue;
	}
}
